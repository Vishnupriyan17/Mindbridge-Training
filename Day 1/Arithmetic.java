public class Arithmetic {
    public static void main(String[] args)
    {
        int Tamil= 87;
        int English = 97;
        System.out.println("This is Addition: "+(Tamil+English));
        System.out.println("This is Subtraction: "+(Tamil - English));
        System.out.println("This is Multiplication: "+(Tamil*English));
        System.out.println("This is Division: "+(English/Tamil));

    }
}
